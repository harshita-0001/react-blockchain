//change format of date
function formatedDate(date) {
  let newDate = new Date(date);
  newDate.setHours(0, 0, 0, 0);
  newDate.toDateString();
  return newDate;
}

//filtered data for open Projects
export const openProject = () => {
  // open project are which whose endDate is greater then present date and start date should be less then present date
  return data?.filter((value, i) => {
    let endDate = formatedDate(value?.endDate);
    let startDate = formatedDate(value?.startDate);
    let currentDate = formatedDate(new Date());
    const dates = currentDate >= startDate && currentDate <= endDate;
    return dates;
  });
};
//filtered data for comming soon Projects
export const comingSoonProject = () => {
  return data?.filter((value) => {
    const startDate = formatedDate(value?.startDate);
    const currentDate = formatedDate(new Date());
    const dates = startDate > currentDate;
    return dates;
  });
};
//filtered data for closed soon Projects
export const closedProjects = () => {
  return data?.filter((value) => {
    let endDate = formatedDate(value?.endDate);
    let currentDate = formatedDate(new Date());
    return endDate < currentDate;
  });
};

export function pagination(startIndex,endIndex,data){
    return data.slice(startIndex,endIndex)
}

export function searchData(allData,search){
  const data=allData?.filter((value) => {
    let titleToUpperCase = value?.title?.toUpperCase();
    let searchToUpperCase = search?.toUpperCase();
    if (titleToUpperCase?.includes(searchToUpperCase)) {
      return value;
    }
  })
  return data;
}